package fi.hsl.jore.importer.feature.digiroad.entity;

/**
 * Defines the direction of the stop on infrastructure link. Possible values are:
 *
 * <ul>
 *   <li><code>BACKWARD</code>. The stop is against the digitizing direction of the infrastructure link.
 *   <li><code>FORWARD</code>. The stop is in the digitizing direction of the infrastructure link.
 * </ul>
 */
public enum DigiroadStopDirection {
    BACKWARD,
    FORWARD
}
